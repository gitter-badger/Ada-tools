namespace AdaTools {
	/// <summary>
	/// Represents the different types of Ada program representations
	/// </summary>
	public enum ProgramType {
		/// <summary>
		/// A function program, which returns a value to the runtime
		/// </summary>
		Function,

		/// <summary>
		/// A procedure program, which does not return a value to the runtime
		/// </summary>
		Procedure,
	}
}
